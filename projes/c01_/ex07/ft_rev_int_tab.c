void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	glass;

	i = 0;
	while (i < (size / 2))
	{
		glass = tab[i];
		tab[i] = tab[size - i - 1];
		tab[size - i - 1] = glass;
		i++;
	}
}
