#include "utils.h"

int		*copy_stack(int *stack, int size)
{
	int *copy;
	int i;

	i = 0;
	copy = init_b(size + 1);
	while (i <= LEN)
	{
		copy[i] = stack[i];
		i++;
	}
	return (copy);
}
