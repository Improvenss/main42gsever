#include "libft.h"

char	*ft_strndup(const char *src, size_t n)
{
	char			*dest;
	unsigned int	i;

	if (!(dest = (char*)malloc(sizeof(char) * (n + 1))))
		return (NULL);
	i = 0;
	while (i < n)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
