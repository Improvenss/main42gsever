int		ft_power(int nb, int power)
{
	int r;

	r = 1;
	if (power > 0)
	{
		r = ft_power(nb, power - 1);
		r = r * nb;
	}
	if (r == 1 && power < 0)
		r = 0;
	return (r);
}
