#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*so;

	if (!(so = ft_strnew(len)) || !s)
		return (NULL);
	so = ft_strncpy(so, (char *)s + start, len);
	return (so);
}
