#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned int i;

	i = 0;
	while (i < n && ((unsigned char *)s)[i] != (unsigned char)c)
		i++;
	if ((i == n) && i >= 1 && (((unsigned char *)s)[i - 1] != (unsigned char)c))
		return (NULL);
	return ((unsigned char *)s + i);
}
