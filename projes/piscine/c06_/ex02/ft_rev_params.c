#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	v;

	v = ac - 1;
	while (v > 0)
	{
		i = 0;
		while (av && av[v][i])
			write(1, &av[v][i++], 1);
		write(1, "\n", 1);
		v--;
	}
	return (0);
}
